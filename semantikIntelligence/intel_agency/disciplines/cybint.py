from semantikIntelligence.intel_agency.nucleus import Discipline


class CyberIntelligence(Discipline):
    """
    Cyber intelligence (CYBINT), sometimes called "digital network intelligence (DNINT)", are gathered from cyberspace.
    CYBINT can be considered as a subset of OSINT.
    """

    def __init__(self):
        self.acronym = 'CYBINT'
        self.name = 'Cyber Intelligence'
